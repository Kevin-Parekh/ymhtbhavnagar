/* ===== REVEAL ON SCROLL ===== */
const revealTargets = [
  '.why-card', '.about-visual', '.about-text',
  '.sess-card', '.sess-inside', '.sess-note',
  '.seva-quote-box', '.seva-card',
  '.act-card',
  '.join-step', '.join-eligibility',
  '.contact-card',
  '.trust-badge'
];
document.querySelectorAll(revealTargets.join(',')).forEach((el, i) => {
  el.classList.add('reveal');
});

const io = new IntersectionObserver((entries) => {
  entries.forEach((entry, idx) => {
    if (entry.isIntersecting) {
      setTimeout(() => entry.target.classList.add('visible'), idx * 70);
      io.unobserve(entry.target);
    }
  });
}, { threshold: 0.1, rootMargin: '0px 0px -30px 0px' });

document.querySelectorAll('.reveal').forEach(el => io.observe(el));

/* ===== ACTIVE NAV ON SCROLL ===== */
const sections  = document.querySelectorAll('section[id]');
const navLinks  = document.querySelectorAll('.nav-link');

const sio = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      navLinks.forEach(l => { l.style.color = ''; l.style.background = ''; });
      const a = document.querySelector(`.nav-link[href="#${entry.target.id}"]`);
      if (a && !a.classList.contains('nav-cta')) {
        a.style.color = '#FF6B00';
        a.style.background = '#FFF3E8';
      }
    }
  });
}, { threshold: 0.45 });

sections.forEach(s => sio.observe(s));

/* ===== HIDE FLOATING WA BUTTON ON MOBILE (bar handles it) ===== */
function adjustWaFloat() {
  const waFloat = document.querySelector('.wa-float');
  if (!waFloat) return;
  if (window.innerWidth <= 600) {
    waFloat.style.bottom = '88px';
  } else {
    waFloat.style.bottom = '28px';
  }
}
window.addEventListener('resize', adjustWaFloat, { passive: true });
